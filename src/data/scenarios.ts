const hoursAgo = (hours: number) => new Date(Date.now() - hours * 60 * 60 * 1000).toISOString().replace(/\.\d{3}Z$/, "");

const incident = (
  id: string,
  incidentType: string,
  latitude: number,
  longitude: number,
  severity: number,
  description: string,
  ageHours: number,
  verified: boolean,
  similarReports = 0,
  scenarioTag = "scenario",
): Incident => ({
  id,
  incidentType,
  latitude,
  longitude,
  severity,
  description,
  timestamp: hoursAgo(ageHours),
  verified,
  similarReports,
  scenarioTag,
});

